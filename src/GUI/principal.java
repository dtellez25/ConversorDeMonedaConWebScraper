    private void botonRealizarConversionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRealizarConversionActionPerformed
        
        Scraper tablaMoneda = new Scraper();

        //obteber las monedas a utilizar de los comboBoxes
        String monedaEntrada = comboboxFrom.getSelectedItem().toString();
        String monedaSalida = comboboxTo.getSelectedItem().toString();
        
        float mEntrada;
        float mSalida;
        
        float cantidad_a_convertir = Float.parseFloat(editTextCantidad.getText());
        String resultado;
        
        //extraemos la conversion de cada moneda a dolar
        Float ConversionDolardeMonedaEntrante = tablaMoneda.extraerDatosMoneda(monedaEntrada);
        Float ConversionDolardeMonedaSaliente = tablaMoneda.extraerDatosMoneda(monedaSalida);
        
        if(monedaEntrada == "NIO"){
            mEntrada = cantidad_a_convertir/ ConversionDolardeMonedaEntrante;
        }else if(monedaEntrada == "EUR"){
            mEntrada = cantidad_a_convertir/ ConversionDolardeMonedaEntrante;
        }else if(monedaEntrada == "PEN"){
            mEntrada = cantidad_a_convertir/ ConversionDolardeMonedaEntrante;
        }else{
            mEntrada = cantidad_a_convertir/ ConversionDolardeMonedaEntrante; 
        }
        
        // Realizar la conversi√≥n
        if(monedaSalida == "NIO"){
            mSalida = ConversionDolardeMonedaSaliente* mEntrada;
        }else if(monedaSalida == "EUR"){
            mSalida = ConversionDolardeMonedaSaliente* mEntrada;
        }else if(monedaSalida == "PEN"){
            mSalida = ConversionDolardeMonedaSaliente* mEntrada;
        }else{
            mSalida = ConversionDolardeMonedaSaliente* mEntrada; 
        }

        //calculo para que redondear cualquier resultado a 2 decimales
        double digitoResultado = Math.floor(mSalida * 100) / 100;
        
        //conversion del resultado a String
        String res = Double.toString(digitoResultado);
        
        //String con el resultado final
        resultado = cantidad_a_convertir + monedaEntrada + " = " + res + monedaSalida;
        
        labelTextConversion.setText(resultado);
    }//GEN-LAST:event_botonRealizarConversionActionPerformed

